package de.lambdamoo.hex4j.hexmath.layout;

/**
 * This is a helper class to calculate the polygon. Normally it is separate for
 * FLAT_TOPPED AND POINTY_TOPPED hexagons.
 * 
 */
public class OrientationCorners {
	public OrientationCorners(double f0, double f1, double f2, double f3, double b0, double b1, double b2, double b3, double start_angle) {
		this.f0 = f0;
		this.f1 = f1;
		this.f2 = f2;
		this.f3 = f3;
		this.b0 = b0;
		this.b1 = b1;
		this.b2 = b2;
		this.b3 = b3;
		this.start_angle = start_angle;
	}

	public final double f0;
	public final double f1;
	public final double f2;
	public final double f3;
	public final double b0;
	public final double b1;
	public final double b2;
	public final double b3;
	public final double start_angle;
}
